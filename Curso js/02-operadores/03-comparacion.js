// Se utilizan para hacer la comparasion de diferentes variables o cosas

// Se establece una variable
let a = 10;

// Se hace la comparacion si es mayor que 5 (False)
console.log(a>5);
// Se hace otra comparacion para comprobar si es igual o mayor que 5 (True)
console.log(a>=5);

// Se hace la comparacion si es menor que 5 (False)
console.log(a<5);
// Se hace otra comparacion para comprobar si es igual o menor que 5 (True)
console.log(a<=5);

// Se evalua si es igual en valor a 10
console.log(a ==10);
// Se evalua si es distinto a 10
console.log(a !=10);
// Se evalua si es identico en valor y tipo de dato que 10
console.log(a ==='10');
// Se evalua si no es igual en el tipo de dato o valor 
console.log(a !=='10'); 