console.log(true && true);

let mayor = true;
let suscrito = false;

// AND && entre variables
// Evalua que ambos datos den como resultado True para devolver true
console.log('operador and', mayor && suscrito);

// OR || entre variables
// Evalua que cualquiera de los datos sea true para qeu devuelva true
console.log('operador or', mayor || suscrito);

// NOT ! antes de la variable
// Cambia de true a false y al reves
console.log('operador not', ! mayor);
